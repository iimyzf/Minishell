#include "../include/parser.h"

void	lstfree(t_cmd **lst)
{
	t_cmd	*p;

	while (*lst != NULL)
	{
		p = (*lst)-> next;
		free((*lst)->cmd);
		free(*lst);
		*lst = p;
	}
}
