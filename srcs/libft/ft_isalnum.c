#include "libft.h"

int	ft_isalnum(char c)
{
	if (((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || (c >= '0' && c <= '9'))
		|| ((c == 33 || c == 35) || (c >= 37 && c <= 38) || (c >= 42 && c <= 47))
		|| c == '=' || c == '?' || c == '{' || c == '}' || c == '_' || c == ':')
		return (1);
	return (0);
}
